export function getProducts() {
  const stored = localStorage.getItem('mueblesab_products');
  if (stored) {
    try {
      const loaded = JSON.parse(stored);
      let updated = false;
      const healed = loaded.map(p => {
        const initP = initialProducts.find(ip => ip.id === p.id || ip.name === p.name);
        if (initP) {
          let healedP = { ...p };
          ['price', 'hasDiscount', 'discountPercent', 'stars', 'reviews'].forEach(prop => {
            if (healedP[prop] === undefined || healedP[prop] === null) {
              healedP[prop] = initP[prop];
              updated = true;
            }
          });
          return healedP;
        }
        return p;
      });
      if (updated) {
        saveProducts(healed);
        return healed;
      }
      return loaded;
    } catch (e) {
      console.error("Error parsing stored products, resetting to initial", e);
    }
  }
  saveProducts(initialProducts);
  return initialProducts;
}

export function saveProducts(products) {
  localStorage.setItem('mueblesab_products', JSON.stringify(products));
}

// Variables globales de administrador

export function getIsAdmin() {
  return localStorage.getItem('mueblesab_is_admin') === 'true';
}

export function setIsAdmin(isAdmin) {
  if (isAdmin) {
    localStorage.setItem('mueblesab_is_admin', 'true');
  } else {
    localStorage.removeItem('mueblesab_is_admin');
  }
}

// --- CONFIGURACIÓN ADMIN ---
export function getAdminPassword() {
  let pass = localStorage.getItem('mueblesab_admin_pass');
  if (!pass) {
    pass = "MueblesAB2026Admin!!"; // Exactly 20 characters
    localStorage.setItem('mueblesab_admin_pass', pass);
  }
  return pass;
}

export function setAdminPassword(password) {
  localStorage.setItem('mueblesab_admin_pass', password);
}

